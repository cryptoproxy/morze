def alp_to_morze():
    while True:
        message = input('Напишите текст / Write text: \n')
        if message.lower() == 'stop':
            print('Bye ᓚᘏᗢ')
            break
        try:
            send_message = []
            for i in message:
                send_message.append(MORZE_DICT[i])
                morze_trans = ' '.join(i for i in send_message)
            print(morze_trans)
        except:
            print('В тексте содержатся недопустимые символы.')



def morze_to_alp():
    while True:
        message = input('Напишите морзе / Write morze: \n')
        send_message = []
        for i in message.split(' '):
            for l, k in MORZE_DICT.items():
                if i == k:
                    send_message.append(l)
                    break

            morze_trans = ''.join(send_message)

        print(morze_trans.upper())



